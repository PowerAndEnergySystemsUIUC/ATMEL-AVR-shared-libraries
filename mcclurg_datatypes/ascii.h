#define	ETB	23	/*	End of Transmit Block	*/
#define	CAN	24	/*	Cancel	*/
#define	EM	25	/*	End of Medium	*/
#define	SUB	26	/*	Substitute	*/
#define	ESC	27	/*	Escape	*/
#define	FS	28	/*	File Separator	*/
#define	GS	29	/*	Group Separator	*/
